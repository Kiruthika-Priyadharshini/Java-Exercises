package JavaProblemsolving.Basics;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello,\n!");
        System.out.println("Name:\tKiru");
        System.out.println("Hey, \"How are you!\"");
        System.out.println("Backslash:backslash \\ print");
        System.out.println("Single \'s quote printed");
        System.out.println("Carriage Return: Hello World!\r world!");
        System.out.println("Backspace: Hello\bWorld!");
        System.out.println("Form Feed: Hello\fWorld!");
    }

}
