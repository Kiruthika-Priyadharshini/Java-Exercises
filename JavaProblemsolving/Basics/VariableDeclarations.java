package JavaProblemsolving.Basics;

public class VariableDeclarations {
    public static void main(String[] args) {
        int a = 10;
        double b = 5.56;
        char c = 'A';
        boolean d = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        
    }

}
