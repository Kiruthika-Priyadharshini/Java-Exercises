package JavaProblemsolving.Operators;

public class IncrementDecrement {
    public static void main(String[] args) {
        int x= 5;
        int preIncremented=++x;
        int postIncremented=preIncremented++;
        System.out.println(preIncremented);
        System.out.println(postIncremented);
        
    }
     

}
