package JavaProblemsolving.Operators;

public class Relational {
    
    public static void main(String[] args) {
        int a= 10;
        int b=20;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(20<=b);
        System.out.println(10>=b);

        
    } 
}