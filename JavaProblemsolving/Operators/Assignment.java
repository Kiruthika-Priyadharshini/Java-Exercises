package JavaProblemsolving.Operators;

public class Assignment {
    public static void main(String[] args) {
        int num =101;
        System.out.println(num+=10);
        System.out.println(num-=10);
        System.out.println(num*=10);
        System.out.println(num/=10);
        System.out.println(num%=10);
        System.out.println(num&=10);
        System.out.println(num|=10);
        System.out.println(num^=10);
        System.out.println(num>>=10);
        System.out.println(num<<=10);

    }

}
