package JavaProblemsolving.Operators;

public class MixDataTypes {
    public static void main(String[] args) {
        int val1= 10;
        double val2= 10.56;
        double output1=val1+val2;
        System.out.println(output1);
        System.out.println(val1+val2);
        System.out.println(val1-val2);
        System.out.println(val1*val2);
        System.out.println(val1/val2);


    }
    

}
