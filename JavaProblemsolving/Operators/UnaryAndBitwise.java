package JavaProblemsolving.Operators;

public class UnaryAndBitwise {
    public static void main(String[] args) {
        int positive =10;
        int negative =-positive;
        System.out.println(positive + ","+ negative);

        int bitwise = 8;
        System.out.println(bitwise>>2);
        System.out.println(bitwise<<2); 
    }
    

}
